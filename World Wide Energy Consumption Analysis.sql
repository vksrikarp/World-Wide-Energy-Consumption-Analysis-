/*Has energy consumption increased or decreased over the years for major economies?*/

SELECT CS.COUNTRY, 
		CS.YEAR,
        SUM(CS.CONSUMPTION) AS CONSUMPTION_BY_YEAR ,
        coalesce(SUM(CS.CONSUMPTION) - LAG(SUM(CS.CONSUMPTION)) OVER(partition by COUNTRY),0)   AS GROWTH
        FROM CONSUMPTION CS
WHERE CS.COUNTRY IN 
		(SELECT C.COUNTRY 
			FROM 
				(SELECT G.Country,  SUM(G.Value) 
                FROM GDP G
				GROUP BY G.Country
				ORDER BY  SUM(G.Value)  DESC
				LIMIT 5) 
		C)
GROUP BY CS.COUNTRY, CS.YEAR; 
