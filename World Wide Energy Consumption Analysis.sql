/*What is the average yearly change 
in emissions per capita for each country?*/
SELECT PC.country,  AVG(PC.YEARLY_CHANGE) AS AVG_YEARLY_CHANGE
FROM 
	(SELECT country,  year,  
		SUM(per_capita_emission) AS Per_capita_Per_year,
        coalesce(SUM(per_capita_emission) - LAG(SUM(per_capita_emission)) OVER(PARTITION BY YEAR),0) AS YEARLY_CHANGE
		FROM EMISSIONS
        GROUP BY country,  year,  
		per_capita_emission
		ORDER BY COUNTRY
	) PC
GROUP BY PC.country
HAVING AVG(PC.YEARLY_CHANGE) != 0
ORDER BY AVG(PC.YEARLY_CHANGE) DESC;

